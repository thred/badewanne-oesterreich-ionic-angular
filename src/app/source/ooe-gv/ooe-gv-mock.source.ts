export class OoeGvSourceMock extends OoeGvSource {
    refreshCount: number = 4;

    override get name(): string {
        return "Hydrographischer Dienst Oberösterreich (Mock)";
    }

    override get disclaimer(): string {
        return "Datenquelle: Mock of data.ooe.gv.at";
    }

    override get minimumPollingInterval(): number {
        return 1000 * 20;
    }

    override async getAllStations(): Promise<Station[]> {
        return super.getAllStations();
    }

    protected override async fetchData(): Promise<string> {
        Utils.info(`Fetching mocked data ...`);

        if (++this.refreshCount % 5 === 0) {
            throw new Error("Failed to fetch mocked data. This is a simulated error for testing.");
        }

        return new Promise((resolve) => {
            setTimeout(() => {
                resolve(binary);
            }, 5000); // simulated lag
        });
    }

    protected override parseItem(itemBinary: string, polledAt: Date): OoeGvItem | undefined {
        const item: OoeGvItem | undefined = super.parseItem(itemBinary, polledAt);

        if (!item) {
            return item;
        }

        // Randomize samples
        let timestamp = Date.now() - Math.random() * 1000 * 60 * 60 * 24;

        for (const sample of item.samples) {
            sample.temperature = 5 + Math.random() * 20;
            sample.measuredAt = new Date(timestamp);

            timestamp -= 1000 * 60 * 15;
        }

        return item;
    }
}

